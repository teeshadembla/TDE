import workgroupModel from "./Models/workgroupModel.js";
import dotenv from "dotenv";
dotenv.config();

const MONGO_USER =process.env.MONGO_USER
const MONGO_PASS = process.env.MONGO_PASS
const MONGO_URI = `mongodb+srv://${MONGO_USER}:${MONGO_PASS}@cluster0.a7cnmoq.mongodb.net/?retryWrites=true&w=majority&appName=Cluster0`

async function seedWorkgroups(){
    try{
        await mongoose.connect(MONGO_URI);

        await workgroupModel.deleteMany({});
        console.log("Existing workgroups deleted");

        await workgroupModel.insertMany(workgroups);
        console.log("Workgroups inserted successfully");

        process.exit();
    }catch(err){
        console.error("Error seeding workgroups:", err);
    }
}

async function assignChairsToWorkgroups() {
  try {
    await mongoose.connect(MONGO_URI);
    console.log("DB connected");

    // Map: workgroup title -> chair user email
    const chairAssignments = [
      {
        workgroupTitle: "Tech Policy & Governance",
        chairEmail: "chair1@email.com"
      },
      {
        workgroupTitle: "Digital Assets and Blockchain",
        chairEmail: "chair2@email.com"
      },
      {
        workgroupTitle: "Sustainability in Tech",
        chairEmail: "chair3@email.com"
      }
    ];

    for (const assignment of chairAssignments) {

      // fetch chair user
      const user = await UserModel.findOne({ email: assignment.chairEmail });

      if (!user) {
        console.log(`User not found: ${assignment.chairEmail}`);
        continue;
      }

      // push into chairs array (no duplicates)
      await workgroupModel.updateOne(
        { title: assignment.workgroupTitle },
        { $addToSet: { chairs: user._id } } 
      );

      console.log(`Assigned ${assignment.chairEmail} → ${assignment.workgroupTitle}`);
    }

    console.log("Chair assignment completed");
    process.exit();

  } catch (err) {
    console.error("Error assigning chairs:", err);
    process.exit(1);
  }
}


